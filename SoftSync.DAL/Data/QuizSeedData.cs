    /// <summary>All seed questions across every skill, with derived stable ids.</summary>
    public static AssessmentQuestion[] Questions()
    {
        var list = new List<AssessmentQuestion>();
        foreach (var (skillId, qs) in BySkill)
        {
            for (var i = 0; i < qs.Length; i++)
            {
                list.Add(new AssessmentQuestion
                {
                    Id = skillId * 100 + (i + 1),
                    SkillId = skillId,
                    QuestionText = qs[i].Text,
                    QuestionTextVi = qs[i].TextVi,
                    Type = qs[i].Type
                });
            }
        }
        return list.ToArray();
    }

    /// <summary>All seed options across every question, with derived stable ids.</summary>
    public static AssessmentOption[] Options()
    {
        var list = new List<AssessmentOption>();
        foreach (var (skillId, qs) in BySkill)
        {
            for (var i = 0; i < qs.Length; i++)
            {
                var qid = skillId * 100 + (i + 1);
                var opts = qs[i].Options;
                for (var j = 0; j < opts.Length; j++)
                {
                    list.Add(new AssessmentOption
                    {
                        Id = qid * 10 + (j + 1),
                        QuestionId = qid,
                        OptionText = opts[j].Text,
                        OptionTextVi = opts[j].TextVi,
                        ScoreValue = opts[j].Score
                    });
                }
            }
        }
        return list.ToArray();
    }
